
public class testarCondicional {

	public static void main(String[] args) {
		System.out.println("Teste condicionais");
		int idade = 15;
		
		if (idade >= 18) {
			System.out.println("Você tem mais de 18 anos!");
		}
		else if (idade <= 17) {
			System.out.println("Você tem menos de 18 anos!");
		}
	}
}
